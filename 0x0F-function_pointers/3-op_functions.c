/**
 * op_add - adds two int
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subst two int
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two int
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two int
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of two int
 * @a: int 1
 * @b: int 2
 *
 * Return: int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
